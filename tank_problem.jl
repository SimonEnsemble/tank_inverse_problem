function process_file(file)
	data = CSV.read(file, DataFrame)
	
	rename!(data, ["Time [s]", "liquid_level_reading"])
	
	id = argmax(data[:, "liquid_level_reading"])
	data = data[id:end, :]
	data[:, "Time [s]"] = data[:, "Time [s]"] .- data[1, "Time [s]"]
	
